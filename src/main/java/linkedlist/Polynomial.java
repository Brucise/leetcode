public class Polynomial {
    /**
     * 系数
     */
    private double coefficient;

    /**
     * 指数
     */
    private int index;

    public double getCoefficient() {
        return coefficient;
    }

    public void setCoefficient(double coefficient) {
        this.coefficient = coefficient;
    }

    public int getIndex() {
        return index;
    }

    public void setIndex(int index) {
        this.index = index;
    }
}
